#include <stdio.h>

int main()
{
    int n,flag=0;
   int a[n];
   scanf("%d",&n);
   if((n>0)&&(n<=1000))
   {
   for(int i=0;i<n;i++)
   {
       scanf("%d",&a[i]);
   }
   for(int i=0;i<n;i++)
   {
      if(a[i]!=a[n-i-1])
      {
          flag=1;
          break;
      }
   }
   if(flag)
   {
       printf("No");
   }
   else
   {
      printf("Yes");
       
   }
   }
    return 0;
}
